    public int getID(){
        ResultSet rs = null;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
         Connection conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/NewProject", "root", "Pass1234");
          String sql  = "select max(RestaurantID) from Users";
          Statement st = conn.createStatement();
          rs = st.executeQuery(sql);
          while(rs.next()){
              
              id = rs.getInt(1);
              id++;
          }
        }
        catch(Exception e){
            e.printStackTrace();
        }
        return id;
    }
    
    
    boolean validation(){
        restName = txt_RestName.getText();
        restAddress = txt_RestAddress.getText();
        restManager = cb_restManager.getSelectedItem().toString();
        
        if (restName.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter firstName ");
            return false;
        }
        if (restAddress.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter firstName ");
            return false;
        }
        return true;
    }
    
