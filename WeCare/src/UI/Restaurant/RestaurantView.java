    private void populateRestTable() {
        DefaultTableModel model = (DefaultTableModel) jTblRestaurantDetails.getModel();
         model.setRowCount(0);
        for (RestDetails rd : restDirectory.getRestaurantDirectory()) {
              Object[] row = new Object[4];
            row[0] = rd.getRestName();
            row[1] = rd.getRestAddress();
            Person restmanager = rd.getRestManager();         
            row[2] =  restmanager.getUserName();
            row[3] = rd.getRestPhoneNo();
            
          
            
             model.addRow(row);
        }
    }
